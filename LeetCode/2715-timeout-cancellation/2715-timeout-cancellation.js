/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // 실행을 지연시키기 위해 setTimeout을 사용
    const timeoutId = setTimeout(() => {
        fn(...args); // 지연이 끝나면 함수 실행
    }, t);

    // 취소할 수 있는 cancelFn 반환
    return function cancelFn() {
        clearTimeout(timeoutId); // 지연된 실행을 취소
    };
};

